print('\nЗАДАНИЕ2\nПользователь вводит с клавиатуры три числа. В зависимости от выбора пользователя программа выводит на экран максимум из трёх, минимум из трёх или среднеарифметическое трёх чисел\n\nРЕШЕНИЕ:')
sum = 0
for _ in range(len(val)):
    val[_] = Num(f'Ввведите число {_ + 1}')
    sum += val[_]

# Сортируем массив от меньшего к большему
for i in range(len(val)):
    for j in range(i, len(val)):
        if val[i] > val[j]:
           _ = val[i]
           val[i] = val[j]
           val[j] = _

print('\nДействие:\n\t1: максимум из трех чисел\n\t2: минимум из трех чисел\n\t3: среднеарифметическое трех чисел')
go = Num('Введите нужное действие')
match go:
    case 1: print(f'\nМаксимальное из трех чисел: {val[2]}')
    case 2: print(f'\nМинимальное из трех чисел: {val[0]}')
    case 3: print(f'\nСреднеарифметическое из трех чисел: {sum / 3}')
    case _: print(f'\nОшибка! Вы ввели неправильное действие!')

