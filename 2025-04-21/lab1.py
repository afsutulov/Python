#!/usr/bin/python3

# Функция ввода числа с проверкой
def Num(txt):
    while True:
        _ = input(f'{txt}: ')
        if _.isnumeric(): return int(_)
        else: print('Ошибка: Введенные данные не содержат чило!\n')

# Задание 1
print('ЗАДАНИЕ1\nПользователь вводит с клавиатуры три числа. В зависимости от выбора пользователя программа выводит на экран сумму трёх чисел или произведение трёх чисел.\n\nРЕШЕНИЕ:')

val = [0, 0, 0]
for _ in range(len(val)):
    val[_] = Num(f'Ввведите число {_ + 1}')
print('\nДействие:\n\t1: сумма трех чисел\n\t2: произведение трех чисел')
go = Num('Введите нужное действие')
match go:
    case 1: print(f'\nСумма трех чисел: {val[0] + val[1] + val[2]}')
    case 2: print(f'\nПроизведение трех чисел: {val[0] * val[1] * val[2]}')
    case _: print(f'\nОшибка! Вы ввели неправильное действие!')

